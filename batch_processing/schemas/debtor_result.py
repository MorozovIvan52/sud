class DebtorResult:
    """Результат обработки одного должника — все 46 полей."""

    def __init__(self, **kwargs: Any):
        self._data: dict[str, str] = {
            col: str(kwargs.get(col, "")) for col in DEBTOR_RESULT_COLUMNS
        }
        for k, v in kwargs.items():
            if k in DEBTOR_RESULT_COLUMNS:
                self._data[k] = str(v) if v is not None else ""

    def to_row_dict(self) -> dict[str, str]:
        """Словарь для записи в Excel/CSV (ключи — названия колонок)."""
        return dict(self._data)

    def __getitem__(self, key: str) -> str:
        return self._data.get(key, "")

    @classmethod
    def from_court_details(cls, details: dict[str, str]) -> "DebtorResult":
        """Создать из словаря court_details.build_court_details()."""
        return cls(**{k: (v or "") for k, v in details.items()})
