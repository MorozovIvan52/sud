def infer_region_from_city(address: str) -> Optional[str]:
    """Если в строке нет «область/край/респ», но есть известный город — возвращаем субъект РФ."""
    if not address or not address.strip():
        return None
    al = address.lower()
    for city_key, reg in _CITY_TO_REGION:
        if city_key in al:
            return reg
    return None


def extract_district(address: str, region: Optional[str]) -> Optional[str]:
    """
    Извлекает район из структуры адреса.
    Паттерны: «X район», «X судебный район», «город X», «г X», «посёлок X», «рп X».
    """
    if not address or not address.strip():
        return None
    addr = address.strip()
    al = addr.lower()

    # Москва / СПб — улица → район (заглушки)
    if region == "Москва":
        if "тверская" in al or "ленина" in al:
            return "Тверской"
        if "арбат" in al:
            return "Арбатский"
    if region == "Санкт-Петербург":
        if "невский" in al:
            return "Центральный"

    # «X район», «X судебный район», «X р-н»
    m = re.search(
        r"(?:^|[,;\s])([А-Яа-яёЁ\-]+(?:ов|ев|ий|ой|ский|ый|ной))\s+(?:судебный\s+)?район(?:а)?(?:\s+\([^)]+\))?",
        addr,
        re.IGNORECASE,
    )
    if m:
        d = m.group(1).strip()
        if d.lower() not in ("й", "ый", "ой"):
            return d

    m = re.search(r"([А-Яа-яёЁ\-]+(?:ов|ев|ий|ой|ский|ый))\s+р-н\b", addr, re.IGNORECASE)
    if m:
        return m.group(1).strip()

    # «город X», «г X» — до запятой (чтобы «г Нижний Новгород, пр-кт…» давал полное название города)
    m = re.search(r"(?:^|[,;\s])(?:город|гор\.?|г\.?)\s+([^,;]+)", addr, re.IGNORECASE)
    if m:
        city = m.group(1).strip()
        city = re.sub(r"\s+", " ", city)
        if len(city) > 2 and city.lower() not in ("обл", "край", "респ"):
            return city

    # «посёлок X», «рп X», «п X»
    m = re.search(r"(?:^|[,;\s])(?:посёлок|пос\.?|рп\.?|п\.?)\s+([А-Яа-яёЁ\-]+)", addr, re.IGNORECASE)
    if m:
        return m.group(1).strip()

    # «село X», «с X»
    m = re.search(r"(?:^|[,;\s])(?:село|с\.?)\s+([А-Яа-яёЁ\-]+)", addr, re.IGNORECASE)
    if m:
        return m.group(1).strip()

    # Первый значимый топоним после индекса (часто город)
    parts = re.split(r"[,;]", addr, maxsplit=5)
    for p in parts[1:3] if len(parts) > 1 else []:
        p = p.strip()
        if not p or re.match(r"^\d{6}$", p):
            continue
        p = re.sub(r"^\d{6}\s*,?\s*", "", p).strip() or p
        if not p:
            continue
        skip = ("обл", "край", "респ", "ао", "область", "р-н")
        if p.lower() in skip or any(p.lower().endswith(" " + s) for s in skip):
            continue
        if len(p) > 2 and re.match(r"^[А-Яа-яёЁ\-\s]+$", p):
            return p

    return None


def normalize_address(addr: str) -> str:
    """Нормализация адреса: пробелы, запятые."""
    if not addr:
        return ""
    return addr.strip().replace(".,", ",").replace("  ", " ").strip()


def parse_address(address: str) -> Dict[str, Optional[str]]:
    """
    Извлекает из адреса: region, district, normalized.
    region — каноническое имя (Республика Башкортостан, Хабаровский край, Оренбургская область и т.д.).
    district — район, город или населённый пункт для поиска суда.
    """
    address = (address or "").strip()
    if not address:
        return {"raw": address, "normalized": "", "region": None, "district": None}

    normalized = normalize_address(address)
    region = extract_region(address)
    district = extract_district(address, region)
    if not region:
        region = infer_region_from_city(address)

    return {
        "raw": address,
        "normalized": normalized,
        "region": region,
        "district": district,
    }


def address_lemmas(address: str) -> List[str]:
    """Леммы токенов адреса (spaCy); пустой список, если NLP не установлен."""
    try:
        from court_locator.ru_nlp import spacy_lemmas

        return spacy_lemmas(address or "")
    except Exception:
        return []
