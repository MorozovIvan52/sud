def _extract_region_canonical(addr: str) -> Optional[str]:
    """Извлекает регион и приводит к каноническому виду (совместимо с courts.region)."""
    if not addr or not addr.strip():
        return None
    a = addr
    al = a.lower()
    for pattern, canonical in REGION_ALIASES:
        m = re.search(pattern, a, re.IGNORECASE)
        if m:
            if canonical:
                return canonical
            # Группа для подстановки (например "Республика X", "X край")
            if "республика" in pattern.lower():
                return f"Республика {m.group(1).strip()}"
            if "край" in pattern.lower() or "ао" in pattern.lower():
                return m.group(1).strip() + (" край" if "край" in pattern else " АО")
            if "обл" in pattern.lower() or "область" in pattern.lower():
                s = m.group(1).strip()
                return s + (" область" if not s.endswith("область") else s)
    return None


def extract_region(address: str) -> Optional[str]:
    """Извлекает регион из адреса (каноническое имя для поиска в БД)."""
    return _extract_region_canonical(address)


